'''
This module contains math operations.
'''


def plus(a_num, b_num):
    '''Function do addition of numbers.'''
    return a_num + b_num


def minus(a_num, b_num):
    '''Function do substraction of numbers.'''
    return a_num - b_num


def multi(a_num, b_num):
    '''Function do multiplication of numbers.'''
    return a_num * b_num


def divis(a_num, b_num):
    '''Function do division of numbers.'''
    return a_num / b_num


def power(a_num, b_num):
    '''Function do powering of numbers.'''
    return a_num ** b_num
